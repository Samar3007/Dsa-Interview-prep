class Solution {
  public:
    int setBits(int N) {
        int num=0;
        num+=N%2;
        if(num) N=N-1;
        while(N){
            num+=N&1;
            N=N>>1;
            
        }
        return num;
    }
};

//{ Driver Code Starts.
int main() {
    int t;
    cin >> t;
    while (t--) {
        int N;
        cin >> N;

        Solution ob;
        int cnt = ob.setBits(N);
        cout << cnt << endl;
    }
    return 0;
}

